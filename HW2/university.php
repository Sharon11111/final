社團:在大學我參加了交大風城吉他社，也參與了許多社團表演。因為我喜歡唱歌，所以在社團表演中都是擔任主唱，培養了在台上和觀眾互動，並且穩穩地完成表演的能力。
<br>
系上:我參與了許多系上的活動，包括兩屆的資財營，在15屆中擔任組輔和接了許多表演和主持；16屆中擔任幹部-執秘，又更深層的參與營隊運作。
<br>
除了資財營還有迎新擔任組輔長，並也擔任組輔和接了許多表演活動。還有參加許多其他系上活動，像是資財之夜、系烤...也接了許多表演。
<br>在系上參與、舉辦了許多活動，我擁有了更好的團隊合作能力，且能夠承擔壓力和責任，產出更好的表演和活動。
